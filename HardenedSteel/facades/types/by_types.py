def is_instance_of_integer(
        value
) -> bool:
    return isinstance(
        value,
        int
    )


def is_instance_of_float(
        value
) -> bool:
    return isinstance(
        value,
        float
    )


def is_instance_of_string(
        value
) -> bool:
    return isinstance(
        value,
        str
    )

